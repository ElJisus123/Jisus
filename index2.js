let n1 = 4;
let n2 = 2;
let operacion = "+"

if (operacion === "+") {
    let resultado = n1 + n2;
    console.log(resultado)
}
else if (operacion === "-") {
    let resultado = n1 - n2
    console.log(resultado)
}
else if (operacion === "*") {
    let resultado = n1 * n2
    console.log(resultado)
}
else if (operacion === "/") {
    let resultado = n1 / n2
    console.log(resultado)
}
